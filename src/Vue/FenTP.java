    private void executeHQLQuery(String query) {

        try {
            Session session = Utils.HibernateUtil.getSessionFactory().openSession();
            session.beginTransaction();
            Query q = session.createQuery(query);
            List resultat = q.list();
            afficherResultat(resultat);
            session.getTransaction().commit();

        } catch (HibernateException he) {
            he.printStackTrace();
        }
    }

    private void afficherResultat(List listing) {
        Vector<String> enteteTable = new Vector<String>();
        Vector donneesTable = new Vector();
        //initialiser les entetes avec le bin texte

        enteteTable.add("Prénom");
        enteteTable.add("Nom");
        enteteTable.add("Date Embauche");
        enteteTable.add("Salaire");

        //remplir la table une ligne a la fois
        for (Object o : listing) {
            Employees emp = (Employees) o;
            //mettre le contenu dans une ligne de la table
            Vector<Object> ligne = new Vector<Object>();
            ligne.add(emp.getFirstName());
            ligne.add(emp.getLastName());
            ligne.add(emp.getHireDate());
            ligne.add(emp.getSalary());
            //ajouter la ligne dans table UI
            donneesTable.add(ligne);
        }
        table.setModel(new DefaultTableModel(donneesTable, enteteTable));

    }

    public void remplirCombLoc() {
        Session session = Utils.HibernateUtil.getSessionFactory().openSession();
        session.beginTransaction();
        Query qE = session.getNamedQuery("queryEmp");
        Query qD = session.getNamedQuery("queryDep");
        Query qJ = session.getNamedQuery("queryEmpJob");

        Query q = session.getNamedQuery("queryLocaltion");

        List listeE = qE.list();
        List listeD = qD.list();
        List listeL = q.list();
        List listeJ = qJ.list();

        champIdLoc.removeAllItems();
        for (Object o : listeL) {
            Locations loc = (Locations) o;
            champIdLoc.addItem(loc.getLocationId() + "-" + loc.getCity());

        }
        champDepEmp.removeAllItems();

        for (Object o : listeD) {
            Departments dep = (Departments) o;
            champDepEmp.addItem(dep.getDepartmentId() + "-" + dep.getDepartmentName());

        }
        champIdMan.removeAllItems();
        champManEMP.removeAllItems();
        champIdMan.addItem("");
        champManEMP.addItem("");

        for (Object o : listeE) {
            Employees emp = (Employees) o;
            champIdMan.addItem(emp.getEmployeeId() + "-" + emp.getFirstName() + ", " + emp.getLastName());
            champManEMP.addItem(emp.getEmployeeId() + "-" + emp.getFirstName() + ", " + emp.getLastName());
            //champJobEmp.addItem(emp.getJobId());
        }
        champJobEmp.removeAllItems();

        for (int i = 0; i < listeJ.size(); i++) {
            champJobEmp.addItem(listeJ.get(i));

        }
    }

