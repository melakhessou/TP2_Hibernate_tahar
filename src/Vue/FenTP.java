    private void btnAjouterDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterDepActionPerformed
        // TODO add your handling code here:

        if (champNameDep.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Vous-devez entrer le nom de département S.V.P !", "Erreur de saisie", JOptionPane.INFORMATION_MESSAGE);

        } else {
            String[] tokens = new String[2];

            String locationD = champIdLoc.getSelectedItem().toString();
            tokens = locationD.split("-");
            int idLoc = Integer.parseInt(tokens[0]);
            String ville = tokens[1];

            // Créer objets session et transaction
            java.util.logging.Logger.getLogger("org.hibernate").setLevel(Level.SEVERE);
            Session session = HibernateUtil.currentSession();
            Transaction tx = session.beginTransaction();

            Locations location = new Locations((short) idLoc, ville);

            Departments dep = new Departments();
            dep.setDepartmentName(champNameDep.getText());
            dep.setLocations(location);
            String mangerD = champIdMan.getSelectedItem().toString();

            if (!mangerD.isEmpty()) {
                tokens = mangerD.split("-");
                int idMan = Integer.parseInt(tokens[0]);
                Employees employe = new Employees(idMan);
                dep.setEmployees(employe);

            }

            session.saveOrUpdate(dep);
            tx.commit();
            remplirCombLoc();
            champNameDep.setText("");
            JOptionPane.showMessageDialog(null, "Ajout avec succès", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAjouterDepActionPerformed

    private void btnAjouterEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterEActionPerformed
        // TODO add your handling code here:
        /**
         * **************Employees**************
         */
        if (champNomEmp.getText().isEmpty() || champPrenomEmp.getText().isEmpty() || champEmail.getText().isEmpty()
                || champSalaire.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Vous-devez remplir tous les chmps S.V.P !", "Erreur de saisie", JOptionPane.INFORMATION_MESSAGE);
        } else {

            String[] tokens = new String[2];

            String depEmp = champDepEmp.getSelectedItem().toString();
            tokens = depEmp.split("-");
            int idDepEmp = Integer.parseInt(tokens[0]);
            //System.out.println(idDepEmp);

            String jobEmp = champJobEmp.getSelectedItem().toString();

            Employees emp = new Employees();
            emp.setFirstName(champPrenomEmp.getText());
            emp.setLastName(champNomEmp.getText());

            DateFormat format = new SimpleDateFormat("dd-mm-yyyy");
            Date date;

            try {
                date = format.parse(champDateE.getText());
                emp.setHireDate(date);
            } catch (ParseException ex) {
                //Logger.getLogger(FenTP.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "date invalide !", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
            }
            if (!Utils.Utils.isDouble(champSalaire.getText())) {
                JOptionPane.showMessageDialog(null, "Salaire invalide !", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
            } else {
                BigDecimal b = new BigDecimal(champSalaire.getText());

                emp.setSalary(b);

                emp.setJobId(jobEmp);
                Departments dep = new Departments();
                dep.setDepartmentId((short) idDepEmp);
                emp.setDepartments(dep);
                int idManEmp;
                String manEmp = champManEMP.getSelectedItem().toString();
                if (!manEmp.isEmpty()) {
                    tokens = manEmp.split("-");
                    idManEmp = Integer.parseInt(tokens[0]);

                    Employees manager = new Employees();
                    manager.setEmployeeId(idManEmp);
                    emp.setEmployees(manager);

                }
                // Créer objets session et transaction
                java.util.logging.Logger.getLogger("org.hibernate").setLevel(Level.SEVERE);
                Session session = HibernateUtil.currentSession();

                try {
                    if (HibernateUtil.isEmailAdress(champEmail.getText())) {
                        emp.setEmail(champEmail.getText());
                        Transaction tx = session.beginTransaction();
                        session.saveOrUpdate(emp);
                        tx.commit();
                        JOptionPane.showMessageDialog(null, "Ajout avec succès", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
                        remplirCombLoc();
                        champNomEmp.setText("");
                        champPrenomEmp.setText("");
                        champEmail.setText("");
                        champDateE.setText("");
                        champSalaire.setText("");

                    } else {
                        JOptionPane.showMessageDialog(null, "email invalide !", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "email invalide !", "Erreur de saisie", JOptionPane.ERROR_MESSAGE);
                }

            }
        }

    }//GEN-LAST:event_btnAjouterEActionPerformed

